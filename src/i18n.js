let currentLang = localStorage.getItem('ytdl-lang') || 'fr';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || translations.fr[key] || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('ytdl-lang', lang);
  document.documentElement.lang = lang;
}

function getLang() {
  return currentLang;
}
